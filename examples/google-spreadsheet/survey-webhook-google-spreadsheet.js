function doPost(e){
	sheet = SpreadsheetApp.getActiveSheet();

	var request = {
		domain: 'https://www.surveycake.com/webhook/v0/',
		svid: e ? e.parameter.svid : '',
		hash: e ? e.parameter.hash : ''
	};

	if (!request.svid || !request.hash) {
		return returnResponse(false, 'MISSING_PARAMS');
	}

	var webhookUrl = request.domain + request.svid + '/' + request.hash;
	var response = UrlFetchApp.fetch(webhookUrl);
	var dat = response.getContentText();
	if (!dat) {
		return returnResponse(false, 'FETCH_ANSWER_FAILED');
	}

	var cipherParams = CryptoJS.lib.CipherParams.create({
		ciphertext: CryptoJS.enc.Base64.parse(dat),
	});
	var decrypted = CryptoJS.AES.decrypt(
		cipherParams,
		CryptoJS.enc.Utf8.parse(setting.key),
		{
			iv: CryptoJS.enc.Utf8.parse(setting.iv),
		}
	);
	var result = JSON.parse( decrypted.toString(CryptoJS.enc.Utf8) );
	if (!result) {
		return returnResponse(false, 'FETCH_DATA_FORMAT_INCORRECT');
	}

	/**
	 * update the spreadsheet
	 */
	var subjects = [];
	var sns = [];
	var answers = [];
	result.result.forEach(function (res) {
		subjects.push(res.subject);
		sns.push(res.sn);
		answers.push(res.answer.join(','));
	});

	/**
	 * add 2 more column
	 */
	sns.push('SUBMIT_TIME');
	sns.push('POST_HASH');
	answers.push(result.submitTime);
	answers.push(request.hash);

	/**
	 * detect isFirstAnswer
	 */
	var lastRow = sheet.getLastRow();
	var lastColumn = sheet.getLastColumn();
	var isFirstAnswer = lastRow == 0;
	if (isFirstAnswer) {
		sheet.appendRow(sns);
		sheet.appendRow(subjects);
		sheet.setFrozenRows(2);

		lastRow += 2;

		sheet.getRange('A1:1').setNumberFormat('0');
	} else {
		/**
		 * Detect Subject Changes
		 */
		var originSn = sheet.getRange('A1:1').getValues()[0]
			.map(function(value) {
				if (typeof value === 'number') {
					return parseInt(value);
				}
				return value;
			})
			.filter(function(value) {
				return value !== undefined && value !== '';
			});

		/**
		 * moving sn position as new sn array
		 */
		var deletedSns = originSn.filter(function(sn) {
			return sns.indexOf(sn) === -1;
		});

		if (deletedSns.length > 0) {
			deletedSns.forEach(function(value, index) {
				var columnIndex = findColumnIndex(value);
				moveColumn(columnIndex, lastColumn);
			});
		}

		sns.forEach(function(sn, index) {
			var columnIndex = findColumnIndex(sn);
			
			if (columnIndex != (index + 1)) {
				if (columnIndex === undefined) {
					/**
					 * new subjects
					 */
					sheet.insertColumnBefore(index+1);
					sheet.getRange(1, index+1).setValue(sn);
					sheet.getRange(2, index+1).setValue(subjects[index]);
				} else {
					/**
					 * re-ordered subjects
					 */
					moveColumn(columnIndex, index+1);
				}
			}
		});
	}

	/**
	 * append new answer to corect order
	 */
	var newRowIndex = lastRow + 1;
	sheet.appendRow(answers);

	/**
	 * set all alignment to left
 	 * getRange(row, column, numRows, numColumns)
	 */
	var lastColumn = sheet.getLastColumn();
	sheet.getRange(1, 1, newRowIndex, lastColumn).setHorizontalAlignment('left');

	return returnResponse(true, 'SUCCESS_APPEND_ANSWER_ROW');
}

