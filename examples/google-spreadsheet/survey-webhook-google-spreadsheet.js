var sheet;
var setting = {
	key: 'YOUR_SURVEY_HASH_KEY',
	iv: 'YOUR_SURVEY_IV_KEY'
};

function returnResponse(status, message) {
	return ContentService.createTextOutput(JSON.stringify({
		status: status,
		message: message,
	})).setMimeType(ContentService.MimeType.JSON); 
}

function findColumnIndex(columnKey){
	var dataRange = sheet.getRange('A1:1');
	var rowData = dataRange.getValues()[0];
	var result = undefined;

	rowData.forEach(function(value, index) {
		if (value == columnKey && result === undefined) {
			result =  index + 1;
		}
	});

	return result;
}

function moveColumn(fromIndex, toIndex) {
	if (fromIndex < toIndex) {
		var source = sheet.getRange(1, fromIndex, sheet.getMaxRows(), 1);
		var target = sheet.getRange(1, toIndex + 1);
		sheet.insertColumnAfter(toIndex);
		source.copyTo(target);
		sheet.deleteColumn(fromIndex);
	} else if (fromIndex > toIndex) {
		var source = sheet.getRange(1, fromIndex + 1, sheet.getMaxRows(), 1);
		var target = sheet.getRange(1, toIndex);
		sheet.insertColumnBefore(toIndex);
		source.copyTo(target);
		sheet.deleteColumn(fromIndex + 1);
	}
}

